package com.fairlink.passenger.bean;

public class BookInfo {
	private String bookId;
	private String bookPosterPath;
	private String bookName;
	private String bookAuthor;
	private String bookTypeSetting;
	private String bookDisplaySetting;
	public String getBookId() {
		return bookId;
	}
	public void setBookId(String bookId) {
		this.bookId = bookId;
	}
	public String getBookPosterPath() {
		return bookPosterPath;
	}
	public void setBookPosterPath(String bookPosterPath) {
		this.bookPosterPath = bookPosterPath;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getBookAuthor() {
		return bookAuthor;
	}
	public void setBookAuthor(String bookAuthor) {
		this.bookAuthor = bookAuthor;
	}
	public String getBookTypeSetting() {
		return bookTypeSetting;
	}
	public void setBookTypeSetting(String bookTypeSetting) {
		this.bookTypeSetting = bookTypeSetting;
	}
	public String getBookDisplaySetting() {
		return bookDisplaySetting;
	}
	public void setBookDisplaySetting(String bookDisplaySetting) {
		this.bookDisplaySetting = bookDisplaySetting;
	}

}